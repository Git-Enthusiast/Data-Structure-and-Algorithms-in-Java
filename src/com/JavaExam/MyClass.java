package com.JavaExam;

public class MyClass {
    // Method with default access level (package-private)
    void defaultMethod() {
        // Can be accessed within the same package
    }

    // Method with public access level
    public void publicMethod() {
        // Can be accessed from any other class
    }

    // Method with private access level
    private void privateMethod() {
        // Can be accessed only within this class
    }

    // Method with protected access level
    protected void protectedMethod() {
        // Can be accessed within the same package and by subclasses
    }
}

